<p>Кроме того, выполняем установку «под ключ». Сюда входят: демонтаж старых окон, «теплые» откосы, установка подоконников, отливов. Предлагаем алюминиевые балконные системы, москитные сетки любого размера, средства для ухода за окнами ПВХ, балконные ручки-защелки. Также у нас можно заказать ремонт старых пластиковых окон после окончания гарантийного срока, ламинацию.</p>

